package com.apkupdater.event;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class DownloadCompleteEvent
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private long code;
    private boolean success;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public DownloadCompleteEvent(
        boolean success,
        long code
    ) {
        this.code = code;
        this.success = success;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public long getCode(
    ) {
        return code;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public boolean isSuccess() {
        return success;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}

